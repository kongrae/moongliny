function Media({
  item,
  className = "",
  isActive = true,
  shouldLoad = true
}) {
  if (!shouldLoad) {
    return React.createElement("div", {
      className: "modal-media-placeholder",
      "aria-hidden": "true"
    });
  }
  if (item.type === "video") {
    const auto = Boolean(item.autoPlay);
    return React.createElement("video", {
      className: className,
      src: item.src,
      controls: !auto,
      playsInline: true,
      preload: "metadata",
      autoPlay: auto && isActive,
      muted: auto,
      loop: auto,
      disablePictureInPicture: auto,
      controlsList: auto ? "nodownload noplaybackrate noremoteplayback" : "nodownload"
    });
  }
  return React.createElement("img", {
    className: className,
    src: item.src || item.image,
    alt: item.alt || item.title || "",
    loading: isActive ? "eager" : "lazy",
    decoding: "async"
  });
}
